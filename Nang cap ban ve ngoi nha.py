import turtle 
t = turtle
pen_color = "#CCCCFF"

# Ve ngoi nha 
t.penup()
t.pencolor(pen_color)
t.pensize(5)
t.goto(-100,0)
t.pendown()
	# Phan than ngoi nha 
t.forward(200)
t.left(90)
t.forward(100)
t.left(90)
t.forward(200)
t.left(90)
t.forward(100)
t.left(90)
	# phan mai ngoi nha
t.pensize(5)
t.goto(-100,100)
t.pendown()
t.left(45)
t.forward(100)
t.right(90)
t.forward(100)
	# phan cua ngoi nha 
t.penup()
t.goto(-25,0)
t.pendown()
t.left(135)
t.forward(70)
t.right(90)
t.forward(50)
t.right(90)
t.forward(70)
	# ong khoi 
