import turtle
t = turtle
# Vẽ cạnh đầu tiên dài 200
t.forward(200)
# Chuyển hướng nét vẽ về bên trái góc 90 độ
t.left(90)
# Vẽ cạnh thứ hai dài 100
t.forward(100)
# Chuyển hướng nét vẽ về bên trái góc 90 độ
t.left(90)
# Vẽ cạnh thứ ba dài 200
t.forward(200)
# Chuyển hướng nét vẽ về bên trái góc 90 độ
t.left(90)
# Vẽ cạnh thứ tư dài 100
t.forward(100)
# Chuyển hướng nét vẽ về bên trái góc 90 độ
t.left(90)
t.done()